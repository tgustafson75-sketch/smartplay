/**
 * Normalize a text string into a canonical form for clip matching.
 *
 * - lowercase
 * - trim
 * - strip trailing punctuation (".", "!", "?", ",") so "Good shot" and
 *   "Good shot." collide as the same recording
 * - collapse internal whitespace
 *
 * Intentionally NOT fuzzy — exact normalized match only. Fuzzy matching
 * would risk firing a recording on the wrong phrase (e.g., "Good drive
 * to the left" matching "Good drive."), which would feel like a bug.
 */
export function normalizeForMatch(text: string): string {
  return text
    .toLowerCase()
    .trim()
    .replace(/[.!?,]+$/g, '')
    .replace(/\s+/g, ' ')
    .trim();
}

/**
 * Build a Map of normalized phrase text → phrase id for O(1) lookup.
 * Memoized at module level — the catalog is static.
 */
let _normalizedIndex: Map<string, string> | null = null;
function getNormalizedIndex(): Map<string, string> {
  if (_normalizedIndex) return _normalizedIndex;
  const m = new Map<string, string>();
  for (const p of CUSTOM_CADDIE_PHRASES) {
    m.set(normalizeForMatch(p.text), p.id);
  }
  _normalizedIndex = m;
  return m;
}

/**
 * Look up the recording URI for a given text, if the user has a clip
 * for the matching phrase. Returns null when:
 *   - text doesn't normalize to any catalog entry
 *   - user has no recording for the matched phrase id
 *   - clips map is missing / empty
 *
 * Caller (voiceService.speak) is responsible for the useCustomCaddie
 * gate — this helper does NOT consult settings.
 */
export function lookupClipUri(
  text: string,
  clips: Record<string, string> | null | undefined,
): string | null {
  if (!clips) return null;
  const norm = normalizeForMatch(text);
  const phraseId = getNormalizedIndex().get(norm);
  if (!phraseId) return null;
  const uri = clips[phraseId];
  return typeof uri === 'string' && uri.length > 0 ? uri : null;
}

/** Phrases grouped by category for UI rendering. */
export function phrasesByCategory(): Record<CustomCaddiePhrase['category'], CustomCaddiePhrase[]> {
  const out: Record<CustomCaddiePhrase['category'], CustomCaddiePhrase[]> = {
    greeting: [], reaction: [], encouragement: [], closing: [],
  };
  for (const p of CUSTOM_CADDIE_PHRASES) out[p.category].push(p);
  return out;
}
