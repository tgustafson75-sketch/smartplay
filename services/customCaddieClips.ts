/**
 * 2026-05-26 — Fix DY: Personal Caddie recorded voice clips.
 *
 * Tim wants the personal-caddie persona to be able to use the user's
 * OWN voice for short fixed phrases (greetings + verbal reactions),
 * with OpenAI TTS as the fallback for anything outside this catalog
 * AND for live conversational responses (which can't be pre-recorded).
 *
 * Architecture:
 *   - Fixed catalog of phrases (below). Order = display order in UI.
 *   - Each phrase has a stable `id` so the recorded file is keyed by
 *     id, not by the (mutable) text. Renaming a phrase keeps the
 *     existing recording valid.
 *   - Lookup: normalizeForMatch(text) → canonical form → maps phrase
 *     id. voiceService.speak() checks this BEFORE calling /api/voice.
 *   - Files live in FileSystem.documentDirectory + 'customCaddieClips/'
 *     (NOT cache — these are user-created assets that survive eviction).
 *   - URIs persisted on playerProfileStore.customCaddieClips keyed by
 *     phrase id; the file itself is the source of truth, the URI is
 *     just a pointer.
 *
 * Not in v1 (deferred):
 *   - Cloud backup of clips (matches the device-local-only library
 *     situation tracked in [[cloud-backup-roadmap]]).
 *   - Free-form recording (record any phrase). Catalog-only keeps the
 *     lookup deterministic — no fuzzy matching, no surprise misfires.
 *   - Per-persona clips (currently scoped to Use My Custom Caddie ON).
 */

export interface CustomCaddiePhrase {
  /** Stable identifier — never rename. */
  id: string;
  /** What the user will hear / record. Exact text the caddie also speaks. */
  text: string;
  /** Short hint for the UI ("when the caddie greets you in the morning"). */
  hint: string;
  /** Display category — used for section grouping in the recorder UI. */
  category: 'greeting' | 'reaction' | 'encouragement' | 'closing';
}

